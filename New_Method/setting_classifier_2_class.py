def data_read(fn, inp, gt):  # csv_read.py
    # Input from .csv
    doc = csv.reader(open(fn, 'r'))
    for line in doc:
        line.pop(19)
        b = [line[40]]

        # convert data to numeric
        data_convert(line)

        # convert to float
        a = list(map(float, line))
        attack_convert(b)

        # add converted data to List
        inp.append(a)
        gt.append(b)

    # data regularization
    data_check(inp)


def data_check(all_data):  # data_ops.py
    max_val = np.max(all_data, axis=0)
    min_val = np.min(all_data, axis=0)
    ave = np.average(all_data, axis=0)

    for line_num in range(len(all_data)):
        line = all_data[line_num]
        for i in range(len(line)):
            if max_val[i] != min_val[i]:
                line[i] = (line[i] - min_val[i]) / (max_val[i] - min_val[i])

    print("max :\n%s" % max_val)
    print("min :\n%s" % min_val)
    print("ave :\n%s\n" % ave)
